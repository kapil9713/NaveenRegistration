package BrowserUtil;

public class Locators {

	public static void main(String[] args) {
		
		for(int i=0;i<=10;i++) {
			if(i%2==0) {
				System.out.println("Even="+i);
			}
			else {
				System.out.println("Odd="+i);
			}
		}
	System.out.println("-------------------------------");
	
	for(int i=1;i<=10;i++) {
		System.out.println("Table of 3="+i*3);
	}
		
		

	}

}
